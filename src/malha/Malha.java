package malha;

public class Malha {
	public Trecho[] trechos;
	public Composicao[] composicoes;
	public Chave[] chaves;
	
	public Trecho[] getTrechos() {
		return trechos;
	}
	public void setTrechos(Trecho[] trechos) {
		this.trechos = trechos;
	}
	public Composicao[] getComposicoes() {
		return composicoes;
	}
	public void setComposicoes(Composicao[] composicoes) {
		this.composicoes = composicoes;
	}
	public Chave[] getChaves() {
		return chaves;
	}
	public void setChaves(Chave[] chaves) {
		this.chaves = chaves;
	}
	
}
